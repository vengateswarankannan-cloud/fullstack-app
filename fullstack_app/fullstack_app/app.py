@app.route("/upload", methods=["POST"])
@login_required
def upload_files():
    user_id = session["user_id"]
    user_name = secure_filename(session.get("user_name", "user")).replace(" ", "_")

    uploaded_any = False
    conn = get_db_connection()
    cur = conn.cursor()

    for field_name in ("file1", "file2"):
        file = request.files.get(field_name)
        if not file or file.filename == "":
            continue

        if not allowed_file(file.filename):
            flash(f"{field_name}: only PDF, PNG, or JPEG files are allowed.", "error")
            continue

        ext = file.filename.rsplit(".", 1)[1].lower()
        timestamp = datetime.now().strftime("%Y%m%d%H%M%S")
        safe_original = secure_filename(file.filename)
        final_filename = f"{user_name}_{timestamp}_{safe_original}"

        filepath = os.path.join(app.config["UPLOAD_FOLDER"], final_filename)
        file.save(filepath)

        cur.execute(
            """INSERT INTO uploaded_files (user_id, filename, file_type, upload_time)
               VALUES (%s, %s, %s, %s)""",
            (user_id, final_filename, ext, datetime.now()),
        )
        uploaded_any = True

    conn.commit()
    cur.close()
    conn.close()

    if uploaded_any:
        flash("File(s) uploaded successfully.", "success")
    else:
        flash("No valid files were uploaded. Allowed types: PDF, PNG, JPEG.", "error")

    return redirect(url_for("dashboard"))


