@app.route("/dashboard")
@login_required
def dashboard():
    conn = get_db_connection()
    cur = conn.cursor()
    cur.execute(
        "SELECT id, filename, file_type, upload_time FROM uploaded_files "
        "WHERE user_id = %s ORDER BY upload_time DESC",
        (session["user_id"],),
    )
    files = cur.fetchall()
    cur.close()
    conn.close()
    return render_template("dashboard.html", user_name=session.get("user_name"), files=files)


