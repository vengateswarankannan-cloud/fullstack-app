@app.route("/signup", methods=["GET", "POST"])
def signup():
    if request.method == "POST":
        name = request.form.get("name", "").strip()
        age = request.form.get("age", "").strip()
        address = request.form.get("address", "").strip()
        email = request.form.get("email", "").strip().lower()
        mobile = request.form.get("mobile", "").strip()
        password = request.form.get("password", "")
        confirm_password = request.form.get("confirm_password", "")

        # ---- Validation ----
        if not all([name, age, address, email, mobile, password, confirm_password]):
            flash("All fields are required.", "error")
            return redirect(url_for("signup"))

        if not age.isdigit() or not (1 <= int(age) <= 120):
            flash("Please enter a valid age.", "error")
            return redirect(url_for("signup"))

        if not EMAIL_REGEX.match(email):
            flash("Please enter a valid email address.", "error")
            return redirect(url_for("signup"))

        if not MOBILE_REGEX.match(mobile):
            flash("Mobile number must be exactly 10 digits.", "error")
            return redirect(url_for("signup"))

        if len(password) < 6:
            flash("Password must be at least 6 characters long.", "error")
            return redirect(url_for("signup"))

        if password != confirm_password:
            flash("Passwords do not match.", "error")
            return redirect(url_for("signup"))

        try:
            conn = get_db_connection()
            cur = conn.cursor()
        except Exception as db_err:
            flash(f"Database Connection Failed! Make sure MySQL is started. Error: {str(db_err)}", "error")
            return redirect(url_for("signup"))

        try:
            # Check duplicates
            cur.execute(
                "SELECT id FROM users WHERE email = %s OR mobile_number = %s",
                (email, mobile),
            )
            if cur.fetchone():
                flash("An account with this email or mobile number already exists.", "error")
                return redirect(url_for("signup"))

            password_hash = generate_password_hash(password)
            cur.execute(
                """INSERT INTO users (name, age, address, email, mobile_number, password_hash)
                   VALUES (%s, %s, %s, %s, %s, %s)""",
                (name, int(age), address, email, mobile, password_hash),
            )
            conn.commit()
            flash("Account created successfully! Please log in.", "success")
            return redirect(url_for("login"))
        except Exception as e:
            conn.rollback()
            flash(f"Something went wrong: {str(e)}", "error")
            return redirect(url_for("signup"))
        finally:
            cur.close()
            conn.close()

    return render_template("signup.html")


